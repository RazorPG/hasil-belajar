#include <stdio.h>

void main()
{
	int a = 1; //true
	int b = 0; //false
	
	printf("a = %d\n", a);
	printf("b = %d\n", b);
	
	printf("a && b = %d\n", a && b); //logika and
	printf("a || b = %d\n", a || b); //logika or
	printf("!a = %d\n", !a); //logika not
	printf("a ^ b =%d\n", a ^ b); //logika xor
	
}
